package com.wat.melody.common.utils.exception;

public class IllegalListingOptionsException extends MelodyException {

	private static final long serialVersionUID = -4198408978489494132L;

	public IllegalListingOptionsException() {
		super();
	}

	public IllegalListingOptionsException(String msg) {
		super(msg);
	}

	public IllegalListingOptionsException(String msg, Throwable cause) {
		super(msg, cause);
	}

}