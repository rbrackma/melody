package com.wat.melody.common.files.exception;

public class IllegalXMLFileException extends IllegalFileException {

	private static final long serialVersionUID = -3446065168406584060L;

	public IllegalXMLFileException() {
		super();
	}

	public IllegalXMLFileException(String msg) {
		super(msg);
	}

	public IllegalXMLFileException(Throwable cause) {
		super(cause);
	}

	public IllegalXMLFileException(String msg, Throwable cause) {
		super(msg, cause);
	}

}